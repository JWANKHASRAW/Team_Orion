package Jwan;

public class OddAndEvenNumber {
    public static void main(String[] args) {

        OddOrEven(6);
    }

    public static void OddOrEven(int number){


        if(number %2==0){
            System.out.println(number + " is an even number");

        }else{
            System.out.println(number + " is an odd number");

        }


    }

}
