package Jwan;

public class Swap_Numbers {
    public static void main(String[] args) {


        double num1 = 4.5;
        double num2 = -2;

        num1 = num1*num2; // 1*2= 2
        num2 = num1/num2; // 2/2=1
        System.out.println("num2 = " + num2 );
        num1 = num1/num2; // 2/1 = 2
        System.out.println("num1 = " + num1 );





    }
}
